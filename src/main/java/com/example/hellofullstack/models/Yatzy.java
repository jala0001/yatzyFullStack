package com.example.hellofullstack.models;



public class Yatzy {
   private String playerName;
   private String ones;
   private String twos;
   private String threes;
   private String fours;
   private String fives;
   private String sixes;

   public Yatzy() {

   }

    public String getPlayerName() {
        return playerName;
    }

    public void setPlayerName(String playerName) {
        this.playerName = playerName;
    }

    public String getOnes() {
        return ones;
    }

    public void setOnes(String ones) {
        this.ones = ones;
    }

    public String getTwos() {
        return twos;
    }

    public void setTwos(String twos) {
        this.twos = twos;
    }

    public String getThrees() {
        return threes;
    }

    public void setThrees(String threes) {
        this.threes = threes;
    }

    public String getFours() {
        return fours;
    }

    public void setFours(String fours) {
        this.fours = fours;
    }

    public String getFives() {
        return fives;
    }

    public void setFives(String fives) {
        this.fives = fives;
    }

    public String getSixes() {
        return sixes;
    }

    public void setSixes(String sixes) {
        this.sixes = sixes;
    }
}
